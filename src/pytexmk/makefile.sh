#!/bin/bash

# 如果没有输入参数，则直接执行 python3 ./pytexmk.py
if [ $# -eq 0 ]; then
    python3 ./pytexmk.py
    exit
fi

# 拼接参数为一个字符串
args="$1"
shift
while [ -n "$1" ]; do
    args="$args $1"
    shift
done

# 运行 makefile
python3 ./pytexmk.py $args
